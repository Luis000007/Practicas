package Java.Introduccion.Practicas.Bucles.While_;

public class tres {
    public static void main (String[] args) {

        /*
        Este codigo va aumentado de 2 en 2, el tope sera hasta el numero 100 por <=

         */

        int x = 0;

        while (x <= 100) {

            System.out.println(x);
            x = x + 2;

        }
    }
}
