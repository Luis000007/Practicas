package Java.Introduccion.Practicas.Tipos_de_variables;

public class Variables {
    public static void main(String[]args){

        /*
        Todos los programas funcionan por variables.
        Una variable permite almacenar un valor asignandolo a un nombre.
        El nombre puede utiliarse para al valor mas adelante en el programa.

        Ejemplo:

        String name;
        name = "James";

        o

        String name = "James";


        Una variable puede cambiar su valor

        String name = "James";
        name = "David";
        System.out.println(name);
         */

        // Tipos de variables

        int age = 42; // Numeros enteros

        double weight = 12.5; // Numeros decimales

        float heigth = 1.19f; // Otra manera de almacenar decimales (Se necesita poner una "f" al final)

        /*
        float utiliza menos memoria pero no es tan preciso como double.
        Si se necesitan calculos precisos utiliza double.
         */

        char letter = 'B'; // Se usan para caracteres (Se utilizan comillas simples)

        boolean isOpen = false; // Contiene valores de true o false

        String namee = "Jesus"; // Almacena letras y numeros enteros y decimales
        String nameee = "Jesus001";
        String nameeee = "Jesus1.1";

    }
}











