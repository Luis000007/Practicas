package Java.Intermedio.Practicas.Math;

public class Valor_Absoluto {
    public static void main(String[] args) {
        int a = Math.abs(10);
        System.out.println(a);

        int b = Math.abs(-20);
        System.out.println(b);
    }
}

    // El valor absoluto de un numero es la distancia que este tenga de 0, sin importar el signo
